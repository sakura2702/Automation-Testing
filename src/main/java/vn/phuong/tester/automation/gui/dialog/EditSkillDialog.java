    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        // TODO add your handling code here:
        if(isCreation) {
            onCreation();
            isEdition = false;
            return;
        } 
        SkillModel model = (SkillModel) jTable.getModel();
        if(isEdition){
            model.editSkill(jTable.getSelectedRow(), panel.mappingElement());
        } else{
            model.insertSkillAt(jTable.getSelectedRow() + 1, panel.mappingElement());
            if(model.getRowCount() != 1)
                jTable.setRowSelectionInterval(jTable.getSelectedRow()+1, jTable.getSelectedRow()+1);
            isEdition = true;
        }
        jTable.addNotify();
        this.jButton1MouseClicked(evt);
    }//GEN-LAST:event_jButton2MouseClicked

    private void onCreation(){
        SkillType type = (SkillType) jComboBox1.getSelectedItem();
        if(SkillType.OPEN.equals(type)){
            replaceMainPanel(new OpenSkill());
        }
        if(SkillType.INPUT.equals(type)){
            replaceMainPanel(new InputSkill());
        }
        if(SkillType.SELECT.equals(type)){
            replaceMainPanel(new SelectionSkill());
        }
        if(SkillType.CLICK.equals(type)){
            replaceMainPanel(new ClickSkill());
        }
        if(SkillType.VERIFY.equals(type)){
            replaceMainPanel(new VerifySkill());
        }
        jButton2.setText(EDIT);
        isCreation = false;
    }
    
