    private void replaceMainPanel(AbstractTestSkill skill){
        GroupLayout layout =  (GroupLayout) getContentPane().getLayout();
          if(skill instanceof OpenSkill){
            panel = new OpenSkillPanel(skill);
        }
        if(skill instanceof SelectionSkill){
            panel = new SelectionPanel(skill);
        }
        if(skill instanceof ClickSkill){
            panel = new ClickPanel(skill);
        }
        if(skill instanceof InputSkill){
            panel = new InputPanel(skill);
        }
        if(skill instanceof VerifySkill){
            panel = new VerifyPanel(skill);
        }
        if(panel != null)
            layout.replace(mainPanel, panel);
    }

